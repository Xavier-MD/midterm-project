$(document).ready(() => {
  let scrollingBox = document.querySelector('#nike .scrolling-box');

  $('#nike .scroll-left').click(function() {
    scrollingBox.scrollLeft -= 400;
  });

  $('#nike .scroll-right').click(function() {
    scrollingBox.scrollLeft += 400;
  });
});

